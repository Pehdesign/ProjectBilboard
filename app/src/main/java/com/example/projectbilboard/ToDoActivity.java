    private List<ToDoItem> refreshItemsFromMobileServiceTable(String field, String query) throws ExecutionException, InterruptedException {
        return mToDoTable.where()
                .field(field).eq(val(query))
                .and()
                .field("complete").eq(val(false))
                .execute().get();
    }


