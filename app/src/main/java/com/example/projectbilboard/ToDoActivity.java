    /**
     * Add a new item
     */
    public void addItem(Intent intent) {
        if (mClient == null) {
            return;
        }

        final ToDoItem item = new ToDoItem();
        item.setText(intent.getStringExtra("Title"));
        item.setOwner(mUser);
        item.setDescription(intent.getStringExtra("Description"));
        item.setImage(intent.getStringExtra("Image"));
        item.setTags(intent.getStringExtra("Tag"));
        item.setRegion(intent.getStringExtra("Region"));
        item.setAdress(intent.getStringExtra("Adress"));
        item.setEmail(intent.getStringExtra("Email"));
        item.setPhone(intent.getStringExtra("Phone"));
        item.setData(intent.getStringExtra("Data"));
        item.setComplete(false);


        // Insert the new item
        AsyncTask<Void, Void, Void> task = new AsyncTask<Void, Void, Void>() {
            @Override
            protected Void doInBackground(Void... params) {
                try {
                    final ToDoItem entity = addItemInTable(item);

                    runOnUiThread(new Runnable() {
                        @Override
                        public void run() {
                            if (!entity.isComplete()) {
                                mAdapter.add(entity);
                            }
                        }
                    });

                } catch (final Exception e) {
                    createAndShowDialogFromTask(e, "Error");
                }
                return null;
            }
        };

        runAsyncTask(task);

        refreshItemsFromTable();
    }

